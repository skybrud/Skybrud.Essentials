namespace Skybrud.Essentials.Strings {

    /// <summary>
    /// Enum class representing the format to be used when converting a <see cref="byte"/> array into a HEX string.
    /// </summary>
    public enum HexFormat {

        /// <summary>
        /// Indicates that the HEX string should be in lower case.
        /// </summary>
        LowerCase,

        /// <summary>
        /// Indicates that the HEX string should be in upper case.
        /// </summary>
        UpperCase

    }

}